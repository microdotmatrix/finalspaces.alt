export async function filterMockQuotesByKeyword(
  keyword: string
): Promise<UnifiedQuote[]> {
  const lower = keyword.toLowerCase();
  return mockQuotes.filter(
    (q) =>
      q.quote.toLowerCase().includes(lower) ||
      q.source.toLowerCase().includes(lower)
  );
}

export async function filterMockQuotesByAuthor(
  author: string
): Promise<UnifiedQuote[]> {
  const lower = author.toLowerCase();
  return mockQuotes.filter((q) => q.author.toLowerCase().includes(lower));
}
